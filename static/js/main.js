function csrfSafeMethod(method) {
    return (/^(GET|HEAD|OPTIONS|TRACE)$/.test(method));
}

function getCookie(name) {
    var cookieValue = null;
    if (document.cookie && document.cookie !== '') {
        var cookies = document.cookie.split(';');
        for (var i = 0; i < cookies.length; i++) {
            var cookie = cookies[i].trim(); // Does this cookie string begin with the name we want?
            if (cookie.substring(0, name.length + 1) === (name + '=')) {
                cookieValue = decodeURIComponent(cookie.substring(name.length + 1));
                break;
            }
        }
    }
    return cookieValue;
}

var csrftoken = getCookie('csrftoken');

function send_beta_user_info(){
    const email = document.getElementsByName("email")[0].value;
    const github_id = document.getElementsByName("github_id")[0].value;
    if(email == '' ||github_id == ''){
      $("#error_msg").text('Input your email or github_id')
    }
    $.ajaxSetup({
        beforeSend: function (xhr, settings) {
            if (!csrfSafeMethod(settings.type) && !this.crossDomain) {
                xhr.setRequestHeader("X-CSRFToken", csrftoken);
            }
        }
    });

    $.ajax({
        url: '/reservation'
        ,method: 'POST'
        ,data:{
            'email': email,
            'github_id': github_id
        }
        , async: false
        ,success: function (data) {
            if (data.status === "success"){
                alert('확인 메일을 보내드렸습니다.\n 등록해 주셔서 감사합니다.');
            } else if (data.status === "registered"){
                alert("이미 등록하셨습니다.");
            }
        },
        error: function (data) {
            alert("실패");
        }
    });
}
